CREATE TABLE PRODUIT (
   PRD_ID INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1),
   PRD_TITRE varchar(100) NOT NULL,
   PRD_DESCRIPTION varchar(2048) NOT NULL,
   PRD_PRIX FLOAT NOT NULL,
   PRD_IMAGE varchar(512) NOT NULL,
   PRIMARY KEY (PRD_ID)
 );
 
 CREATE TABLE UTILISATEUR (
   UTI_ID INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1),
   UTI_LOGIN varchar(100) NOT NULL,
   UTI_PWD varchar(512) NOT NULL,
   PRIMARY KEY (UTI_ID)
 );
 

CREATE TABLE COMMENTAIRE (
   COM_ID INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1),
   COM_CONTENU varchar(20148) NOT NULL,
   PRD_ID INT NOT NULL FOREIGN KEY REFERENCES PRODUIT(PRD_ID),
   UTI_ID INT NOT NULL FOREIGN KEY REFERENCES UTILISATEUR(UTI_ID),
    PRIMARY KEY (COM_ID)
 ); 

 
CREATE TABLE PRD_UTI (
   PRD_ID INT NOT NULL FOREIGN KEY REFERENCES PRODUIT(PRD_ID),
   UTI_ID INT NOT NULL FOREIGN KEY REFERENCES UTILISATEUR(UTI_ID),
   PRIMARY KEY (PRD_ID, UTI_ID)
 );
 